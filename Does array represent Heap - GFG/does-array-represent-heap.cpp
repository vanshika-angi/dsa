class Solution{
    public:
    bool isMaxHeap(int a[], int n)
    {
        for(int i=0;i<n;i++)
        {
            int lt = 2*i+1;
            int rt= 2*i+2;
            if(lt<n && a[i]<a[lt])return false;
            if(rt<n && a[i]<a[rt])return false;
            // if()
        }
        
        return true;
    }
};

