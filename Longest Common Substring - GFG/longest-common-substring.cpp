class Solution{
    public:
    
    int longestCommonSubstr (string text1, string text2, int n, int m)
    {
        // int n=text1.length();
        // int m=text2.length();
        int ans=0;
        int dp[n+1][m+1];
        
        for(int i=0;i<=n;i++)
        {
            dp[i][0]=0;
        }
        for(int i=0;i<=m;i++)
        {
            dp[0][i]=0;
        }
        
        for(int i=1;i<=n;i++)
        {
            for(int j=1;j<=m;j++)
            {
                if(text1[i-1]==text2[j-1])
                {
                    dp[i][j]=dp[i-1][j-1]+1;
                    ans=max(ans,dp[i][j]);
                }
                else
                {
                    dp[i][j]=0;
                }
            }
        }
        
        return ans;
    }
};

